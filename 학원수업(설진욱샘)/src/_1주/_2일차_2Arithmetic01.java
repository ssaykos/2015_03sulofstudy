package _1주;

public class _2일차_2Arithmetic01 {

	public static void main(String[] args) {
		int x=14,y=5;
		System.out.println("'더하기:"+(x+y));
		System.out.println("빼기:"+(x-y));
		System.out.println("곱하기:"+(x*y));
		System.out.println("나누기:"+(x/y));
		//%는 나머지를 구하는 연산자이다.
		// 13%4=1,13/4=3		
		System.out.println("나머지:"+(x%y));

	}

}
